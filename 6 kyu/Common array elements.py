"""
Given three arrays of integers, return the sum of elements that are common in all three arrays.

For example:

common([1,2,3],[5,3,2],[7,3,2]) = 5 because 2 & 3 are common in all 3 arrays
common([1,2,2,3],[5,3,2,2],[7,3,2,2]) = 7 because 2,2 & 3 are common in the 3 arrays
More examples in the test cases.

Good luck!
"""
import random


def timer(func):
    from time import time
    def wrapper(*args, **kwargs):
        start_time = time()
        value = func(*args, **kwargs)
        end_time = time()
        print(f'Runtime = {end_time - start_time} sec.')
        return value
    return wrapper


@timer
def generate_many(x: int = 50000):
    res = []
    for i in range(x):
        res.append(random.randint(0, 10000))
    return res

@timer
def common(a, b, c):
    result = []

    for val in a:
        if val not in result:
            a_count = a.count(val)
            b_count = b.count(val)
            c_count = c.count(val)
            min_count = min(a_count, b_count, c_count)
            if min_count != 0:
                for i in range(min_count):
                    result.append(val)

    return sum(result)

@timer
def common_gpt(a, b, c):
    result = []
    counts = {}

    for val in a:
        if val not in counts:
            counts[val] = [a.count(val), b.count(val), c.count(val)]

    for val, val_counts in counts.items():
        min_count = min(val_counts)
        if min_count != 0:
            result.extend([val]*min_count)

    return sum(result)

